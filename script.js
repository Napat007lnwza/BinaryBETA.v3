// 4.4 ‡πÇ‡∏´‡∏•‡∏î‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°
function loadQuestion() {
    // 1. ‡∏î‡∏∂‡∏á‡∏ä‡∏∏‡∏î‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°‡∏Ç‡∏≠‡∏á Chapter ‡∏ó‡∏µ‡πà‡∏ñ‡∏π‡∏Å‡πÄ‡∏•‡∏∑‡∏≠‡∏Å
    const currentQuestionsSet = chapterQuestions[selectedChapter];

    if (!currentQuestionsSet || currentQuestion >= currentQuestionsSet.length) {
        endGame();
        return;
    }
    const q = currentQuestionsSet[currentQuestion];

    const shuffledOptions = [...q.options];
    shuffleArray(shuffledOptions); // ‚úÖ ‡∏ï‡∏≠‡∏ô‡∏ô‡∏µ‡πâ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ô‡∏µ‡πâ‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß

    const questionEl = document.getElementById("question");
    if(questionEl) questionEl.innerText = q.question;
    
    timeLeft = TIME_LIMIT;
    if(timerCountdownEl) timerCountdownEl.textContent = timeLeft;

    const timerBar = document.getElementById("timer-bar");
    if (timerBar) {
        timerBar.style.width = "100%";
        timerBar.classList.remove("low-time");
    }

    clearInterval(timerInterval);
    timerInterval = setInterval(updateTimer, 1000);

    const buttons = document.querySelectorAll(".answer-btn");
    buttons.forEach((btn, i) => {
        btn.classList.remove("correct", "incorrect", "disabled"); 

        if (shuffledOptions[i] !== undefined) { 
            btn.innerText = shuffledOptions[i];
            btn.onclick = () => checkAnswer(String(shuffledOptions[i]), String(q.answer));
        }
    });
}

