function checkAnswer(selected, correct) {
    clearInterval(timerInterval);

    const buttons = document.querySelectorAll(".answer-btn");

    buttons.forEach(btn => {
        btn.onclick = null;
        btn.classList.add("disabled");

        // ‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö‡∏Ñ‡πà‡∏≤‡πÅ‡∏ö‡∏ö String ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÅ‡∏°‡πà‡∏ô‡∏¢‡∏≥ (‡πÄ‡∏ä‡πà‡∏ô 10 vs "10")
        const btnText = String(btn.innerText);
        
        if (btnText === correct) {
            btn.classList.add("correct");
      } else if (btnText === selected) {
            btn.classList.add("incorrect");
        }
    });
  
    if (selected === correct) {
        score += (1*scoreMultiplier); // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô
      
        // ‚≠ê ‡∏Å‡∏≤‡∏£‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏ó‡∏µ‡πà‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç: ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏ö‡∏ô‡∏´‡∏ô‡πâ‡∏≤‡∏à‡∏≠‡∏ó‡∏±‡∏ô‡∏ó‡∏µ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏ï‡∏≠‡∏ö‡∏ñ‡∏π‡∏Å
        const scoreEl = document.getElementById("score");
        if(scoreEl) scoreEl.innerText = score; 
    }

    setTimeout(() => {
        currentQuestion++;

        const totalQuestions = chapterQuestions[selectedChapter].length;

        if (currentQuestion < totalQuestions) {
            loadQuestion();
        } else {
            endGame();
        }
    }, 1500);
}

